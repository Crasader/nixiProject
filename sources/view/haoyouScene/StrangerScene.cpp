    nc->addObserver(this, SEL_CallFuncO(&StrangerScene::refresh_callback_802), "HTTP_FINISHED_802", NULL);
    nc->addObserver(this, SEL_CallFuncO(&StrangerScene::result_tip), "HTTP_FINISHED_803", NULL);
    
